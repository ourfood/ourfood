package com.ourfood.bean;

public class Foodimg {
	public String fimgid;
	public String fid;
	public String getFimgid() {
		return fimgid;
	}
	public void setFimgid(String fimgid) {
		this.fimgid = fimgid;
	}
	public String getFid() {
		return fid;
	}
	public void setFid(String fid) {
		this.fid = fid;
	}
	public Foodimg() {
		super();
	}
	public Foodimg(String fimgid, String fid) {
		super();
		this.fimgid = fimgid;
		this.fid = fid;
	}
	
	
}
